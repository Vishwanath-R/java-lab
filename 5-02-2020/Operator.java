class Operator
{ 
  public static void main(String args[])
  {
    int a=10;
    int b=5;
    System.out.println(a+"+"+b+"="+ (a+b));
    System.out.println(a+"-"+b+"="+ (a-b));
    System.out.println(a+"*"+b+"="+ (a*b));
    System.out.println(a+"/"+b+"="+ (a/b));
    System.out.println(10<<2); //10*(2^2)=40
    System.out.println(10<<3);
    System.out.println(5<<2); //5*(2^2)=20
    System.out.println(20<<3);
    System.out.println(15<<4);
}}
